int	ft_str_is_lowercase(char *str)
{
	int	res;
	int	i;

	res = 1;
	i = -1;
	while (str[++i])
	{
		if ((str[i] < 'a') || (str[i] > 'z'))
		{
			res = 0;
			break ;
		}
	}
	return (res);
}

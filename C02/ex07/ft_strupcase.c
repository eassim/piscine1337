char	*ft_strupcase(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
	{
		if (str[i] <= 'z' && str[i] >= 'a')
		{
			str[i] -= 'z' - 'Z';
		}
	}
	return (str);
}

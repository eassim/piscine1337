#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	res;
	int	i;

	res = 1;
	i = 0;
	while (str[i])
	{
		if ((str[i] < 'A') || (str[i] > 'Z' && str[i] < 'a') || (str[i] > 'z'))
		{
			res = 0;
			break ;
		}
		i++;
	}
	return (res);
}

char	*ft_strlowcase(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
	{
		if (str[i] <= 'Z' && str[i] >= 'A')
		{
			str[i] -= 'Z' - 'z';
		}
	}
	return (str);
}

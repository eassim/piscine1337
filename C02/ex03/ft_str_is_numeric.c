#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	res;
	int	i;

	res = 1;
	i = -1;
	while (str[++i])
	{
		if ((str[i] < '0') || (str[i] > '9'))
		{
			res = 0;
			break ;
		}
	}
	return (res);
}

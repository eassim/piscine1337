#include <unistd.h>

int	main(void)
{
	int	c;
	int	a;

	c = 'z';
	a = 'a';
	while (c >= a)
	{
		write(1, &c, 1);
		c = c - 33;
		write(1, &c, 1);
		c = c + 31;
	}
	write(1, &"\n", 1);
}

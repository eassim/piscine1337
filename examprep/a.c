#include <unistd.h>

int	main(int n, char **args)
{
	int	i;

	i = 0;
	if (n > 0)
	{
		while(args[n - 1][i])
		{
			if (args[n - 1][i] == 'a')
			{
				write(1, &"a", 1);
				return (0);
			}
			i++;
		}
	}
	return (0);
}

int	ft_atoi(char *str)
{
	int	i;
	int	neg;
	int	nbr;

	nbr = 0;
	i = 0;
	neg = 1;
	while (str[i] && (str[i] == ' ' || (str[i] > 8 && str[i] < 13)))
		i++;
	while (str[i] && (str[i] == '-' || str[i] == '+'
			|| (str[i] >= '0' && str[i] <= '9')))
	{
		if (str[i] == '-')
			neg *= -1;
		if (str[i] >= '0' && str[i] <= '9')
			break ;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = nbr * 10 + (str[i] - '0');
		i++;
	}
	return (nbr * neg);
}

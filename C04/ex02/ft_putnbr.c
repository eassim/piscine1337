#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		power;
	char	c;

	power = 1;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		c = '-';
		write(1, &c, 1);
		nb *= -1;
	}
	while (nb / power > 9)
		power *= 10;
	while (power > 0)
	{
		c = nb / power % 10 + '0';
		write(1, &c, 1);
		power /= 10;
	}
}
